// Lesson 1: Your First Program
// Let's create and describe a sound

// Create a sound with a name
let soundName = "Drums"

// Create a volume level
var volumeLevel = 5

// Print the sound information
print("Sound: \(soundName)")
print("Volume: \(volumeLevel)")
print("")

// We can change the volume using math
print("Turning the volume up...")
volumeLevel = volumeLevel + 2

print("Sound: \(soundName)")
print("Volume: \(volumeLevel)")
print("")

// Now let's print a "Now Playing" message
print("Now Playing: \(soundName) at volume \(volumeLevel)")
print("")

